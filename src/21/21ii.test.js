const solver = require("./21ii");

const input = `...........
.....###.#.
.###.##..#.
..#.#...#..
....#.#....
.##..S####.
.##..#...#.
.......##..
.##.#.####.
.##..##.##.
...........`;

const emptyInput = `...........
...........
...........
...........
...........
.....S.....
...........
...........
...........
...........
...........`;

// const input = `...........
// .####.####.
