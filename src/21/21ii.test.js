// .####.####.
// .####.####.
// .####S####.
// .####.####.
// .####.####.
// .####.####.
// .####.####.
// ...........`;

describe("21ii", () => {
  describe.only("empty field", () => {
    for (const [steps, result] of [
      [10, 11 * 11],
      [49, 50 * 50],
      [199, 200 * 200],
      [999, 1000 * 1000],
    ]) {
      it(`for ${steps} steps`, async () => {
        (await solver(emptyInput, steps)).should.equal(result);
      });
    }
  });

  describe("example", () => {
    for (const [steps, result] of [
      // [50, 1594],
      // [100, 6536],
      // [140, 13023],
      // [500, 167004],
      // [1000, 668697],
      // [5000, 16733044],
    ]) {
      it(`for ${steps} steps`, async () => {
        (await solver(input, steps)).should.equal(result);
      });
    }
  });
});
